def _validate_inventory_tables() -> None:
    names = {row.name for row in _TOOL_ROWS}
    tables: tuple[tuple[str, Mapping[str, object]], ...] = (
        ("discovery", _DISCOVERY),
        ("continuation", _CONTINUATION),
        ("resource alternatives", _RESOURCE_ALTERNATIVES),
        ("prompt alternatives", _PROMPT_ALTERNATIVES),
        ("target routes", _TARGET_ROUTE),
        ("workflow coverage", _WORKFLOW_COVERAGE),
        ("observed use", _OBSERVED_USE),
    )
    for label, table in tables:
        missing = sorted(names - set(table))
        extra = sorted(set(table) - names)
        if missing or extra:
            raise RuntimeError(
                f"MCP declaration {label} table drift: missing={missing}, extra={extra}; "
                f"repair with `{_REPAIR_COMMAND}`"
            )


_validate_inventory_tables()


