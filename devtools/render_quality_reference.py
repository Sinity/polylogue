        "## Artifact Locations",
        "",
        "- mutation campaigns: `artifacts/mutation-campaigns/`",
        "- benchmark campaigns: `artifacts/benchmark-campaigns/`",
        "- pipeline probe manifests and replay bundles: operator-selected paths outside the repo by default",
        "",
        "## Slow-Test Policy",
        "",
        "- keep the default correctness lane representative of real archive/storage invariants",
        "- mark a test `slow` only when it is an optional heavy proof rather than a core correctness contract",
        "- review `pytest --durations` output when a new heavy test appears instead of freezing timing tables in docs",
        "",
