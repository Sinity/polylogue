        "nix develop -c python -m devtools.pipeline_probe --input-mode archive-subset --manifest-in /tmp/polylogue-probe-real.json --stage parse --workdir /tmp/polylogue-probe-replay",
        "```",
        "",
        "### Showcase baseline drift",
        "",
        "```bash",
        "nix develop -c python -m devtools.verify_showcase",
        "nix develop -c python -m devtools.verify_showcase --update",
        "```",
        "",
        "## Validation Lane Catalog",
        "",
        "The lane names below are the stable operator surface. Use the runner rather than copying the underlying pytest command lines into ad-hoc scripts.",
        "",
        "### Contract Lanes",
        "",
        *_render_lane_table(registry.contract_lanes),
        "",
        "### Live Lanes",
        "",
        *_render_lane_table(registry.live_lanes),
        "",
        "### Composite Lanes",
        "",
        *_render_composite_lane_table(registry.composite_lanes),
        "",
        "## Mutation Campaign Catalog",
        "",
        "Durable mutation ledgers live in [mutation-testing-baseline.md](./mutation-testing-baseline.md) and under `artifacts/mutation-campaigns/`.",
        "",
        *_render_mutation_table(registry.mutation_campaigns),
        "",
        "## Benchmark Campaign Catalog",
        "",
        "Benchmark comparisons are intentionally operator-run rather than default hosted-CI jobs.",
        "",
        *_render_benchmark_table(registry.benchmark_campaigns),
