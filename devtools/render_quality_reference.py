        "<!-- Generated by scripts/render-quality-reference. Edit devtools/render_quality_reference.py and devtools/quality_registry.py instead. -->",
        "",
        "# Test Quality Workflows",
        "",
        "This reference is generated from the live validation-lane, mutation-campaign, and benchmark-campaign registries.",
        "",
        "Current registry snapshot:",
        "",
        f"- contract lanes: `{len(registry.contract_lanes)}`",
        f"- live lanes: `{len(registry.live_lanes)}`",
        f"- composite lanes: `{len(registry.composite_lanes)}`",
        f"- mutation campaigns: `{len(registry.mutation_campaigns)}`",
        f"- benchmark campaigns: `{len(registry.benchmark_campaigns)}`",
        "",
        "## Canonical Commands",
        "",
        "### Full correctness run",
        "",
        "```bash",
        "nix develop -c pytest -q -n 0",
        "```",
        "",
        "### Fast local run",
        "",
        "Use this when iterating locally and you do not need the explicit slow proofs or benchmarks.",
        "",
        "```bash",
        'nix develop -c pytest -q -n 0 -m "not slow and not benchmark"',
        "```",
        "",
        "### Validation lanes",
        "",
        "```bash",
        "nix develop -c python -m devtools.run_validation_lanes --list",
        "nix develop -c python -m devtools.run_validation_lanes --lane frontier-local",
        "nix develop -c python -m devtools.run_validation_lanes --lane live-exercises --dry-run",
        "```",
        "",
        "### Mutation campaigns",
        "",
        "```bash",
        "nix develop -c python -m devtools.mutmut_campaign list",
        "nix develop -c python -m devtools.mutmut_campaign run <campaign>",
        "nix develop -c python -m devtools.mutmut_campaign index",
        "```",
        "",
        "### Benchmark campaigns",
        "",
        "```bash",
        "nix develop -c python -m devtools.benchmark_campaign list",
