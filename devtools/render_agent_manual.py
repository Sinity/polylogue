def expected_outputs() -> dict[Path, str]:
    """Return every generated target and its expected UTF-8 text."""

    manual = render_standing_manual()
    reference = render_deep_reference()
    return {
        MANUAL_DATA_PATH: manual,
        REFERENCE_DATA_PATH: reference,
        MANUAL_DOC_PATH: manual,
        REFERENCE_DOC_PATH: reference,
        DATA_DIR / "recipes.json": _json_text(recipe_payload()),
        DATA_DIR / "integration-spec.json": _json_text(integration_spec_payload()),
        DATA_DIR / "tool-contracts.json": _json_text(tool_contract_payload()),
        DATA_DIR / "integration-manifest.json": _json_text(_static_manifest()),
    }


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="Fail when any generated target differs.")
    args = parser.parse_args(argv)
    drift: list[str] = []
    for path, expected in expected_outputs().items():
        actual = path.read_text(encoding="utf-8") if path.exists() else None
        if actual == expected:
            continue
        if args.check:
            drift.append(str(path.relative_to(REPO_ROOT)))
            continue
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(expected, encoding="utf-8")
        print(f"rendered {path.relative_to(REPO_ROOT)}")
    if drift:
        for relative_path in drift:
            print(f"out of date: {relative_path}")
        return 1
    return 0


