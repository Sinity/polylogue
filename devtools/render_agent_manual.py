def _json_text(value: object) -> str:
    return json.dumps(value, ensure_ascii=False, indent=2, sort_keys=True) + "\n"


def _compact_json(value: object) -> str:
    return json.dumps(value, ensure_ascii=False, separators=(",", ":"), sort_keys=True)


def _pretty_json(value: object) -> str:
    return json.dumps(value, ensure_ascii=False, indent=2, sort_keys=True)


def continuation_example_token() -> str:
    """Return a real deterministic token produced by the production codec."""

    request = QueryTransactionRequest(
        operation="query",
        arguments={
            "expression": "actions where action:file_edit AND path:polylogue/archive/query | sort by time desc | limit 20",
            "projection": "action-evidence",
        },
        page_size=20,
        offset=20,
        projection="action-evidence",
        stable_order="time-desc",
    )
    return QueryContinuation(request=request, result_ref="result:0123456789abcdef01234567").encode()


def _call_payload(tool: str, arguments: Mapping[str, object]) -> dict[str, object]:
    return {"name": tool, "arguments": dict(arguments)}


def _fenced_json(payload: object) -> list[str]:
    return ["```json", _pretty_json(payload), "```", ""]


def _semantics(contract_name: str) -> str:
    return ", ".join(item.value for item in TOOL_CONTRACT_BY_NAME[contract_name].result_semantics)


def _render_tool_calls(tool_names: Iterable[str]) -> list[str]:
    lines: list[str] = []
    for name in tool_names:
        contract = TOOL_CONTRACT_BY_NAME[name]
        example = contract.examples[0]
        lines.extend(
            [
                f"### `{name}` — {example.title}",
                "",
                contract.purpose,
                "",
                f"Result semantics declared by the t46.8 source rows: `{_semantics(name)}`.",
                "",
            ]
        )
        lines.extend(_fenced_json(_call_payload(name, example.arguments_dict())))
        lines.extend([f"Expected discipline: {example.result_note}", ""])
    return lines


